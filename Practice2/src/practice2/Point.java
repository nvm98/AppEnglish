
package practice2;

public class Point {
    private int xAxis;
    private int yAxis;
    public Point()
    {
        
    }
    public Point(int x,int y)
    {
        this.xAxis = x;
        this.yAxis = y;
    }
    public void setXAxis(int x)
    {
        this.xAxis = x;
    }
    public int getXAXxis()
    {
        return this.xAxis;
    }
    public void setYAxis(int y)
    {
        this.yAxis = y;
    }
    public int getYAxis()
    {
        return this.yAxis;
    }
}
